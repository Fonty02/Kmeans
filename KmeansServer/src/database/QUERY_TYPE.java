package database;

/**
 * QUERY_TYPE è l'enumeratore che definisce i tipi di query che possono essere effettuate.
 */
public enum QUERY_TYPE {
    /**
     * MIN indica che la query da effettuare è di tipo MIN.
     */
    MIN,
    /**
     * MAX indica che la query da effettuare è di tipo MAX.
     */
    MAX
}
